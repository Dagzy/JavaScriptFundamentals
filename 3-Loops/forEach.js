//For Each loop stuff
var myArr = ['theFirst', 'theSecond', 'theThird']
for (let i = 0; i < myArr.length; i++) {
    console.log(i)//This prints the index that we've selected
    const element = myArr[i]; //This prints the value of the index we've selected by using the [ ] with
    //a variable name in it
    console.log(element)
}